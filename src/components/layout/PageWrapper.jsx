    let cancelled = false;
    (async () => {
      const { runLoader } = await import('../../styles/motion/presets');
      if (cancelled) return;
      cleanup = await runLoader(loader, { onComplete: done });
    })();

    return () => {
      cancelled = true;
      cleanup();
      document.body.style.overflow = '';
    };
